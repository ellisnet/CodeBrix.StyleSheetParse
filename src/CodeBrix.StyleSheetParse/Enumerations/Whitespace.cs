namespace CodeBrix.StyleSheetParse; //Was previously: namespace ExCSS;

/// <summary>Specifies the whitespace options.</summary>
public enum Whitespace : byte
{
    /// <summary>The normal value.</summary>
    Normal,
    /// <summary>The pre value.</summary>
    Pre,
    /// <summary>The no wrap value.</summary>
    NoWrap,
    /// <summary>The pre wrap value.</summary>
    PreWrap,
    /// <summary>The pre line value.</summary>
    PreLine
}
